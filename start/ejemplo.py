# Se define una variable llamada 'texto' que contiene una cadena de texto
texto = "hola, julian velez G."

# Se usa el método 'startswith' para verificar si la cadena comienza con la palabra "hola"
# Este método devuelve True si 'texto' inicia exactamente con "hola", de lo contrario devuelve False
verificar = texto.startswith("hola")

# Se imprime el resultado de la verificación en la consola
print(verificar)  # En este caso, imprime True porque la cadena sí comienza con "hola"
